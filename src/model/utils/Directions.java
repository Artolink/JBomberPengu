package model.utils;

/**
 * Enumeration that defines the direction where the player can move.
 */
public enum Directions {

    /**
     * Up movement.
     */
    UP,

    /**
     * Down movement.
     */
    DOWN,

    /**
     * Left movement.
     */
    LEFT,

    /**
     * Right movement.
     */
    RIGHT,

    /**
     * Stationary (static) movement.
     */
    STATIONARY;
}
